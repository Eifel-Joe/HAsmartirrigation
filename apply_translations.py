for lang in langs:
    path = f"{LANG_DIR}/{lang}.json"
    with open(path, encoding="utf-8") as f:
        lang_data[lang] = json.load(f)

for key_path, translations in T.items():
    keys = key_path.split(".")
    for lang, value in translations.items():
        set_nested(lang_data[lang], keys, value)

